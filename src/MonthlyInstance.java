public class MonthlyInstance {

    String itemName;
    boolean isExpense;
    int quantity;
    int sumOfOne;

    MonthlyInstance(String itemName, boolean isExpense, int quantity, int sumOfOne){
        this.itemName = itemName;
        this.isExpense = isExpense;
        this.quantity = quantity;
        this.sumOfOne = sumOfOne;
    }

}